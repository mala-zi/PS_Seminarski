    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            int selektovanRed = tblMesta.getSelectedRow();
            if (selektovanRed == -1) {
                JOptionPane.showMessageDialog(this, "Ništa nije označeno!", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
            TableModelMesto tmm = (TableModelMesto) tblMesta.getModel();
            Mesto m = tmm.getMesto(selektovanRed);
            try {
                Controller.getInstance().obrisiMesto(m);
            } catch (SQLIntegrityConstraintViolationException ex) {
                JOptionPane.showMessageDialog(this, "Ne možete obrisati mesto jer je vezano za kupca!", "Greška", JOptionPane.ERROR_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(this, "Sistem je uspešno obrisao mesto.", "Obaveštenje", JOptionPane.INFORMATION_MESSAGE);

            TableModelMesto tmodel = new TableModelMesto();
            tblMesta.setModel(tmodel);
            TableColumn idColumn = tblMesta.getColumnModel().getColumn(0);
        idColumn.setPreferredWidth(30);
        idColumn.setMinWidth(20);
        idColumn.setMaxWidth(40);
        } catch (Exception ex) {
            Logger.getLogger(UpravljajCvecarimaForma.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnChangeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeActionPerformed
        // TODO add your handling code here:
        int selektovanRed = tblMesta.getSelectedRow();
        if (selektovanRed == -1) {
            JOptionPane.showMessageDialog(this, "Ništa nije označeno!", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        TableModelMesto tmm = (TableModelMesto) tblMesta.getModel();
        Mesto m = tmm.getMesto(selektovanRed);
        KreirajMestoForma mf = new KreirajMestoForma(this, m);
        mf.setVisible(true);
        TableColumn idColumn = tblMesta.getColumnModel().getColumn(0);
        idColumn.setPreferredWidth(30);
        idColumn.setMinWidth(20);
        idColumn.setMaxWidth(40);
    }//GEN-LAST:event_btnChangeActionPerformed


    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        try {
            if (txtKorisnickoIme.getText().isEmpty() || String.valueOf(txtLozinka.getPassword()).isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!",
                        "Popunite polja", JOptionPane.WARNING_MESSAGE);
            }
            Controller controller = Controller.getInstance();
            String user = txtKorisnickoIme.getText();
            String pass = String.valueOf(txtLozinka.getPassword());
            Cvecar cvecar = new Cvecar();
            cvecar.setKorisnickoIme(user);
            cvecar.setLozinka(pass);
            cvecar = controller.prijaviCvecara(cvecar);
            try {
                //throw new RuntimeException("Simulacija greske");
                JOptionPane.showMessageDialog(this, "Korisničko ime i lozinka su ispravni!", "Prijava", JOptionPane.INFORMATION_MESSAGE);
                Session.getInstance().setUlogovani(cvecar);
                GlavnaForma gf = new GlavnaForma(cvecar);
                gf.setVisible(true);
                this.dispose();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Greška pri otvaranju glavne forme!", "Greška", JOptionPane.ERROR_MESSAGE);
                Controller.getInstance().odjaviCvecara(cvecar.getKorisnickoIme());
            }
        } catch (RuntimeException re) {
            JOptionPane.showMessageDialog(this, re.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

